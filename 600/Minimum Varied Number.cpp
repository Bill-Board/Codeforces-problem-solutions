#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
const int N=200005;
ll mod= 1e9+7;

void solve()
{
    int n, sum = 0, m = 1; cin >> n;
    for(int i = 9; i ; i--){
        if(n < i) continue;
        sum +=  i*m;
        n -= i;
        m *= 10;
    }
    cout << sum << '\n';
}



signed main()
{
    int test_case=1;
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    cin >> test_case;
    while(test_case--)
        solve();
    return 0;
}



