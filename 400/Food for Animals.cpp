#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
const int N=200005;

void solve()
{
    int a,b,c,x,y;
    cin >> a >>b >>c >>x >> y;
    x = max(0, x - a);
    y = max(0, y - b);
    if(x+y>c)
        cout << "NO" << endl;
    else
        cout << "YES" << endl;
}



signed main()
{
    ll test_case=1;
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    cin >> test_case;
    while(test_case--)
        solve();
    return 0;
}



